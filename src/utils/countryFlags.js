export const getCountryFlag = (countryName) => {
    if (!countryName) return 'üè≥Ô∏è';

    // Normalize the input: trim whitespace and convert to title case
    const normalizedName = countryName
        .trim()
        .split(' ')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
        .join(' ');

    // Special cases for country names with articles or abbreviations
    const specialCases = {
        'USA': 'United States',
        'UK': 'United Kingdom',
        'UAE': 'United Arab Emirates',
    };

    const finalName = specialCases[countryName.toUpperCase()] || normalizedName;
    return countryFlags[finalName] || 'üè≥Ô∏è';
};
